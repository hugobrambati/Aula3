# a função range serve para incrementar e decrementar
for a in range(10, 20):  # nesse codigo esta sendo uma função que vai de 10 a 19
    print(a)

for b in range(3, 23, 5):  # nesse codigo esta sendo feita uma função que vai de 3 a 23 pulando de 5 em 5

    print(b)

for c in range(50, 40, -2):  # nesse codigo esta se fazendo um decremento de 2 em 2 de 50 até 40
    print(c)
